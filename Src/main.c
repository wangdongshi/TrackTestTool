/* mainTask function */
void mainTask(void const * argument)
{

  /* USER CODE BEGIN 5 */
  
  assert_param(1 == 1); // for test assert
  PRINTF("Welcome to STM32F407VET6 Core Board!\r\n");
  
  initData();
  startGyro();
  startADC();
  startEncoder(0.0f);
  startCommunication();
  
  while(1) {
    if (workMode == MODE_NORMAL_WORK && trigMode == TRIG_ENCODER) {
      osSemaphoreWait(EncoderArriveSemHandle, osWaitForever);
    }
    else {
      osDelay(500);
    }
    //osSemaphoreRelease(AdcConvertStartSemHandle);
    prepareSensorData();
    osSemaphoreWait(AdcConvertCompleteSemHandle, osWaitForever);
    changeADCData2ActualValue();
    sendData2PC();
  }
  
  /* USER CODE END 5 */ 
}

